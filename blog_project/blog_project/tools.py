def get_date(post):
    return post['date']


def get_all_posts():
    return sorted(posts, key=get_date)


def get_latest_posts():
    return get_all_posts()[-2:]


def get_specific_post(slug):
    try:  # get_object_or_404
        return next(post for post in posts if post['slug'] == slug)
    except:
        raise(Http404(['No such post']))
